# Create a text file named student_data.txt
with open("student_data.txt", "w") as file:
    file.write("Name,Course,Marks\n")
    file.write("John,Data Science,80\n")
    file.write("Alice,Python,90\n")
    file.write("Bob,AI,70\n")
    file.write("Charlie,Machine Learning,85\n")
    file.write("David,Deep Learning,75\n")

# Read the file and display students with marks > 75
with open("student_data.txt", "r") as file:
    for line in file.readlines()[1:]:
        Name, course, marks = line.strip().split(",")
        if int(marks) > 75:
            print(f"Name: {Name}, Course: {course}, Marks: {marks}")