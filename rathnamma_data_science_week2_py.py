# Task 2: Working with Lists and Arrays
import numpy as np

# Create a list
numbers = [12, 45, 7, 23, 56, 89, 34, 1, 6, 8]

# Print the length of the list
print(f"Length of the list: {len(numbers)}")

# Find the maximum and minimum value
print(f"Maximum value: {max(numbers)}")
print(f"Minimum value: {min(numbers)}")

# Add a new element to the list and remove one element
numbers.append(99)
numbers.remove(1)
print(f"Updated list: {numbers}")

# Sort the list
numbers.sort()
print(f"Ascending order: {numbers}")
numbers.sort(reverse=True)
print(f"Descending order: {numbers}")

# Convert the list into a NumPy array
np_array = np.array(numbers)

# calculate Mean, Median, and Standard Deviation
print(f"Mean: {np.mean(np_array)}")
print(f"Median: {np.median(np_array)}")
print(f"Standard Deviation: {np.std(np_array)}")
print("\n")

# Task 3: Dictionaries and Sets

